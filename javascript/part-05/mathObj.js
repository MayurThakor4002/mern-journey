// Math.abs(): Returns the absolute value of a number
console.log(Math.abs(-3432)); // Output: 3432

// Math.pow(): Returns the base to the exponent power (base^exponent)
console.log(Math.pow(2, 5)); // Output: 32

// Math.floor(): Rounds a number down to the nearest integer
console.log(Math.floor(98594.4)); // Output: 98594
console.log(Math.floor(5637.5)); // Output: 5637
console.log(Math.floor(464.80)); // Output: 464
console.log(Math.floor(46)); // Output: 46
console.log(Math.floor(-98594.4)); // Output: -98595
console.log(Math.floor(-5637.5)); // Output: -5638
console.log(Math.floor(-464.80)); // Output: -465

// Math.ceil(): Rounds a number up to the nearest integer
console.log(Math.ceil(98594.4)); // Output: 98595
console.log(Math.ceil(5637.5)); // Output: 5638
console.log(Math.ceil(464.80)); // Output: 465
console.log(Math.ceil(-98594.4)); // Output: -98594
console.log(Math.ceil(-5637.5)); // Output: -5637
console.log(Math.ceil(-464.80)); // Output: -464

// Math.random(): Generates a random floating-point number between 0 (inclusive) and 1 (exclusive)
