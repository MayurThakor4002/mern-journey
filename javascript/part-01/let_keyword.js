// Let keyword 

let age;
age = 20;
console.log("Age is ",age);

age = 20+1;
console.log("Age is ", age);

age = 3;
console.log("Age is ", age);

let num1 = 2;
let num2 = 5;
let sum = num1 + num2;
console.log("Sum is", sum);

let total;
console.log(total);   // undefined
console.log(typeof total); // undefined

total = 100;
console.log(total);  // 100
console.log(typeof total);  // number


// Const keyword

const pi = 3.14;
let radius = 5;
let AreaOfCircle = pi*radius**2;

console.log("Area of circle is ", AreaOfCircle); // 78.5

// var keyword

var language = 2;
console.log(language); // 2

var language = language + 5;
console.log(language); // 7
